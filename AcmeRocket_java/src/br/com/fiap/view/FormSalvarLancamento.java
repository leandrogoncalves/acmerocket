    private void btnSalvarLancamentoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSalvarLancamentoActionPerformed
        String grupo = "";
        CtrlSalvarLancamento ctrlLacamentoo = new CtrlSalvarLancamento();
        CtrlListarGrupo ctrlGrupo = new CtrlListarGrupo();
        Lancamento l = null;

        if (!cmbGrupo.getSelectedItem().equals("") || cmbGrupo.getSelectedIndex() == 0) {

            grupo = String.valueOf(cmbGrupo.getSelectedItem());

        } else {
            //grupo = 0;
        }
        String hora = txtHora.getText();
        String angulo = String.valueOf(txtAngulo.getText());
        String distanciaDoALvo = String.valueOf(txtDistanciaAlvo.getText());
        String velocidadeVento = String.valueOf(txtVelocidadeDoVento.getText());
        String pesoFoguete = String.valueOf(txtPesoFoguete.getText());
        int status = 0;
        float altitudeMaxima = 0;
        float velocidadeMaxima = 0;
        float tempoDePropulsao = 0;
        float picoAceleracao = 0;
        float aceleracaoMedia = 0;
        float tempoApogeuEDescida = 0;
        float tempoEjecao = 0;
        float altiduteEjecao = 0;
        float taxaDecida = 0;
        float duracaoVoo = 0;
        float distanciaQuedaAlvo = 0;

        if (hora.equalsIgnoreCase("") || angulo.equalsIgnoreCase("") || distanciaDoALvo.equalsIgnoreCase("")
                || velocidadeVento.equalsIgnoreCase("") || pesoFoguete.equalsIgnoreCase("")) {

            hora = String.valueOf(0);
            angulo = String.valueOf(0);
            distanciaDoALvo = String.valueOf(0);
            velocidadeVento = String.valueOf(0);
            pesoFoguete = String.valueOf(0);

        }
        
        
        CtrlSalvarLancamento salvar = new CtrlSalvarLancamento(); 
        int validar = salvar.calcularStatus(txtAltitudeMaxima.getText(), txtVelocidadeMaxima.getText(), txtTempoDePropulsao.getText(),
                txtPicoDeAceleracao.getText(),txtAceleracaoMedia.getText(), txtTempoApogeuDescida.getText(),
                txtTempoDeEjecao.getText(), txtAltitudeDeEjecao.getText(), txtTaxaDeDescida.getText(),txtDuracaoVoo.getText(),
                txtQuedaAteAlvo.getText());
        
        if (chkLancamentoFalhou.isSelected()) {
            status = 3;
            //PARA SER LANÇAMENTO OK OS DOIS PRECISA SER VADEIRO
        } else if (validar == 2) {
            status = 2;
            altitudeMaxima = Float.parseFloat(txtAltitudeMaxima.getText());
            velocidadeMaxima = Float.parseFloat(txtVelocidadeMaxima.getText());
            tempoDePropulsao = Float.parseFloat(txtTempoDePropulsao.getText());
            picoAceleracao = Float.parseFloat(txtPicoDeAceleracao.getText());
            aceleracaoMedia = Float.parseFloat(txtAceleracaoMedia.getText());
            tempoApogeuEDescida = Float.parseFloat(txtTempoApogeuDescida.getText());
            tempoEjecao = Float.parseFloat(txtTempoDeEjecao.getText());
            altiduteEjecao = Float.parseFloat(txtAltitudeDeEjecao.getText());
            taxaDecida = Float.parseFloat(txtTaxaDeDescida.getText());
            duracaoVoo = Float.parseFloat(txtDuracaoVoo.getText());
            distanciaQuedaAlvo = Float.parseFloat(txtQuedaAteAlvo.getText());

        } else if (validar == 1) {
            //PRE LANCAMENTO
            status = 1;
        } else if (validar == 0) {
            status = 0;
        }

        if (status != 0) {
            if (this.codigoLancamento == -1) {

                l = new Lancamento(ctrlGrupo.buscarIdComboGrupo(grupo), hora,
                        status, Float.parseFloat(angulo), Float.parseFloat(velocidadeVento),
                        Float.parseFloat(distanciaDoALvo), Float.parseFloat(pesoFoguete), altitudeMaxima,
                        velocidadeMaxima, tempoDePropulsao, picoAceleracao, aceleracaoMedia, tempoApogeuEDescida,
                        tempoEjecao, altiduteEjecao,
                        taxaDecida, duracaoVoo, distanciaQuedaAlvo);

                if (ctrlLacamentoo.inserirLancamento(l)) {
                    this.voltarParaLista();
                }

            } else {
                l = new Lancamento(codigoLancamento, ctrlGrupo.buscarIdComboGrupo(grupo),
                        hora, status, Float.parseFloat(angulo), Float.parseFloat(velocidadeVento),
                        Float.parseFloat(distanciaDoALvo), Float.parseFloat(pesoFoguete), altitudeMaxima,
                        velocidadeMaxima, tempoDePropulsao, picoAceleracao, aceleracaoMedia,
                        tempoApogeuEDescida, tempoEjecao, altiduteEjecao,
                        taxaDecida, duracaoVoo, distanciaQuedaAlvo);

                if (ctrlLacamentoo.editarLancamento(l)) {
                    this.voltarParaLista();
                }
            }
        }

        ctrlLacamentoo = null;
    }//GEN-LAST:event_btnSalvarLancamentoActionPerformed

